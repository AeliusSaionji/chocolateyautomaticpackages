$checksum32  = '53563184abb9c695bb99b1d3f49c3fdf389c6e050b73ddeb52c11476fd1826d5'
$checksum64  = 'b293e26265de557f047c37d64d87c7d06646cbe66809e9803ec7a1668ca3ea51'

$packageArgs = @{
  packageName            = $packageName
  fileType               = 'exe'
  url                    = $url32
  url64bit               = $url64
  silentArgs             = '--silent'
  checksum               = $checksum32
  checksum64             = $checksum64
  checksumType           = 'sha256'
  checksumType64         = 'sha256'
  validExitCodes         = @(0)
  registryUninstallerKey = $packageName
}
